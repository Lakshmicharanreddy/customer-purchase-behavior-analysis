--MONTHLY ORDER TRENDS


SELECT DATE_TRUNC('MONTH',ORDER_PURCHASE_TIMESTAMP::TIMESTAMP) AS ORDER_MONTH,
       COUNT(ORDER_ID) AS TOTAL_ORDERS


FROM ORDERS

GROUP BY DATE_TRUNC('MONTH',ORDER_PURCHASE_TIMESTAMP::TIMESTAMP)
ORDER BY ORDER_MONTH;





--Monthly Revenue Trend



SELECT DATE_TRUNC('MONTH',O.ORDER_PURCHASE_TIMESTAMP::TIMESTAMP) AS ORDER_MONTH,
       SUM(OP.PAYMENT_VALUE) AS TOTAL_REVENUE


FROM ORDERS O
JOIN ORDER_PAYMENTS OP
ON O.ORDER_ID = OP.ORDER_ID

GROUP BY DATE_TRUNC('MONTH',O.ORDER_PURCHASE_TIMESTAMP::TIMESTAMP)
ORDER BY ORDER_MONTH;


-- Average Revenue per Month

SELECT DATE_TRUNC('MONTH',O.ORDER_PURCHASE_TIMESTAMP::TIMESTAMP) AS ORDER_MONTH,
       AVG(OP.PAYMENT_VALUE) AS AVG_REVENUE


FROM ORDERS O
JOIN ORDER_PAYMENTS OP
ON O.ORDER_ID = OP.ORDER_ID

GROUP BY DATE_TRUNC('MONTH',O.ORDER_PURCHASE_TIMESTAMP::TIMESTAMP)
ORDER BY ORDER_MONTH;


--peak sales month


SELECT DATE_TRUNC('MONTH',O.ORDER_PURCHASE_TIMESTAMP::TIMESTAMP) AS ORDER_MONTH,
        SUM(OP.PAYMENT_VALUE) AS TOTAL_REVENUE


FROM ORDERS O
JOIN ORDER_PAYMENTS OP
ON O.ORDER_ID = OP.ORDER_ID

GROUP BY DATE_TRUNC('MONTH',O.ORDER_PURCHASE_TIMESTAMP::TIMESTAMP)
ORDER BY TOTAL_REVENUE DESC
LIMIT 5;





--Orders by Day of Week (Advanced Insight)


WITH DOW_ORDERS AS (

SELECT EXTRACT(DOW FROM ORDER_PURCHASE_TIMESTAMP::TIMESTAMP) AS DAY_OF_THE_WEEK,
	   COUNT(ORDER_ID) AS TOTAL_ORDERS


FROM ORDERS 

GROUP BY EXTRACT(DOW FROM ORDER_PURCHASE_TIMESTAMP::TIMESTAMP)
ORDER BY DAY_OF_THE_WEEK

)
SELECT WEEK,
	   TOTAL_ORDERS

FROM (

SELECT *,
CASE
	WHEN DAY_OF_THE_WEEK = 0 THEN 'SUNDAY'
	WHEN DAY_OF_THE_WEEK = 1 THEN 'MONDAY'
	WHEN DAY_OF_THE_WEEK = 2 THEN 'TUESDAY'
	WHEN DAY_OF_THE_WEEK = 3 THEN 'WEDNESDAY'
	WHEN DAY_OF_THE_WEEK = 4 THEN 'THURSDAY'
	WHEN DAY_OF_THE_WEEK = 5 THEN 'FRIDAY'
	ELSE 'SATURDAY'
END AS WEEK

FROM DOW_ORDERS

)


























































































